    analyzeMessage(
        messageText: string,
        chatType: 'private' | 'group' | 'supergroup' | 'channel',
        userId: string,
        chatId: string
    ): ModerationResponse {

        // Check if moderation is enabled for this chat type
        if (chatType === 'private' && !this.config.enableModerationInPrivate) {
            return this.createIgnoreResponse("Moderation disabled for private chats");
        }

        if ((chatType === 'group' || chatType === 'supergroup') && !this.config.enableModerationInGroups) {
            return this.createIgnoreResponse("Moderation disabled for groups");
        }

        // Analyze message for profanity
        const analysis = this.profanityFilter.analyzeMessage(messageText);

        if (!analysis.hasProfanity) {
            return this.createIgnoreResponse("No profanity detected");
        }

        // Generate appropriate response based on severity
        return this.generateModerationResponse(analysis, chatType, userId);
    }

    private generateModerationResponse(
        analysis: ProfanityAnalysis,
        chatType: string,
        userId: string
    ): ModerationResponse {

        const responseType = this.determineResponseType(analysis);

        if (responseType === 'ignore') {
            return this.createIgnoreResponse("Severity level too low for response");
        }

        const response = this.selectResponse(responseType, analysis);
        const reasoning = this.generateReasoning(analysis, responseType);

        return {
            shouldRespond: true,
            response,
            responseType,
            confidence: analysis.confidence,
            reasoning
        };
    }

    private determineResponseType(analysis: ProfanityAnalysis): ModerationResponse['responseType'] {
        // Check configuration settings
        if (analysis.severityLevel === 'severe' && this.config.strictOnSevere) {
            return 'strict';
        }

        if (analysis.severityLevel === 'moderate' && this.config.moderateOnModerate) {
            return 'moderate';
        }

        if (analysis.severityLevel === 'mild' && this.config.warnOnMild) {
            return 'warning';
        }

        return 'ignore';
    }

    private selectResponse(responseType: Exclude<ModerationResponse['responseType'], 'ignore'>, analysis: ProfanityAnalysis): string {
        const responses = this.config.customResponses[responseType];

        // Select response based on analysis
        if (analysis.language === 'ua') {
            // Prefer Ukrainian responses for Ukrainian profanity
            const ukrainianResponses = responses.filter(r => this.isUkrainianResponse(r));
            if (ukrainianResponses.length > 0) {
                return this.getRandomResponse(ukrainianResponses);
            }
        }

        return this.getRandomResponse(responses);
    }

    private isUkrainianResponse(response: string): boolean {
        // Simple heuristic to detect Ukrainian responses
        const ukrainianWords = ['–¥–∞–≤–∞–π', '–±–µ–∑', '–º–æ–∂–µ–º–æ', '–≤–∞—Ä—Ç–æ', '–ø—Ä–∏–ø–∏–Ω–∏', '–¥–æ—Å–∏—Ç—å', '–Ω–µ–ø—Ä–∏–ø—É—Å—Ç–∏–º–æ'];
        return ukrainianWords.some(word => response.toLowerCase().includes(word));
    }

    private getRandomResponse(responses: string[]): string {
        return responses[Math.floor(Math.random() * responses.length)];
    }

    private generateReasoning(analysis: ProfanityAnalysis, responseType: ModerationResponse['responseType']): string {
        const matchCount = analysis.matches.length;
        const severity = analysis.severityLevel;
        const language = analysis.language;

        let reasoning = `Detected ${matchCount} profanity match${matchCount > 1 ? 'es' : ''} `;
        reasoning += `with ${severity} severity in ${language} language. `;
        reasoning += `Confidence: ${(analysis.confidence * 100).toFixed(1)}%. `;
        reasoning += `Response type: ${responseType}.`;

        if (analysis.matches.length > 0) {
            const words = analysis.matches.map(m => `"${m.word}"`).join(', ');
            reasoning += ` Detected words: ${words}.`;
        }

        return reasoning;
    }

    private createIgnoreResponse(reason: string): ModerationResponse {
        return {
            shouldRespond: false,
            response: '',
            responseType: 'ignore',
            confidence: 0,
            reasoning: reason
        };
    }

    // Configuration methods
    updateConfig(newConfig: Partial<ModerationConfig>) {
        this.config = { ...this.config, ...newConfig };
    }

    getConfig(): ModerationConfig {
        return { ...this.config };
    }

    // Stats and utility methods
    getStats() {
        const filterStats = this.profanityFilter.getStats();
        return {
            profanityFilter: filterStats,
            config: this.config,
            responseTemplates: {
                warning: this.config.customResponses.warning.length,
                moderate: this.config.customResponses.moderate.length,
                strict: this.config.customResponses.strict.length
            }
        };
    }

    // Test method for validation
    testMessage(message: string): { analysis: ProfanityAnalysis; response: ModerationResponse } {
        const analysis = this.profanityFilter.analyzeMessage(message);
        const response = this.generateModerationResponse(analysis, 'group', 'test_user');

        return { analysis, response };
    }

    // Add custom responses
    addCustomResponse(type: keyof ModerationConfig['customResponses'], response: string) {
        this.config.customResponses[type].push(response);
    }

    removeCustomResponse(type: keyof ModerationConfig['customResponses'], response: string) {
        const responses = this.config.customResponses[type];
        const index = responses.indexOf(response);
        if (index > -1) {
            responses.splice(index, 1);
        }
    }

